let heroItems = [];
let currentHeroIndex = 0;
let heroVideoTimeout;
let heroAutoSlideTimeout;
let allItems = [];

document.addEventListener('DOMContentLoaded', () => {
    // Load data from Supabase client
    loadAllData();
});

async function loadAllData() {
    // Phase 1: Render from Cache IMMEDIATELY for instant appearance
    try {
        const cachedSections = JSON.parse(localStorage.getItem('zxko_sections') || '[]');
        const cachedItems = JSON.parse(localStorage.getItem('zxko_items') || '[]');
        const cachedVersions = JSON.parse(localStorage.getItem('zxko_versions') || '[]');

        // Immediate Slider setup from cache
        const sliderCache = cachedItems.filter(i => i.is_slider).sort((a,b) => a.slider_position - b.slider_position);
        if (sliderCache.length > 0) {
            heroItems = sliderCache.map(item => ({
                id: item.id,
                name: { en: item.name_en, ar: item.name_ar },
                tag: item.tag,
                img: item.img_url,
                video: item.slider_video_url,
                version: item.version,
                file_size: item.file_size
            }));
            if (document.getElementById('hero-img')) {
                setHero(0);
            }
        }

        if (cachedSections.length > 0 || cachedItems.length > 0) {
            renderInitialUI(cachedSections, cachedItems, cachedVersions);
        }
    } catch (e) { console.warn("Initial cache render failed", e); }

    // Phase 2: Fetch from Server and update UI in background
    // 1. Fetch Slider
    try {
        const sliderData = await SupabaseClient.getSliderItems();
        heroItems = sliderData.map(item => ({
            id: item.id,
            name: { en: item.name_en, ar: item.name_ar },
            tag: item.tag,
            img: item.img_url,
            video: item.slider_video_url,
            version: item.version,
            file_size: item.file_size
        }));
        if (document.getElementById('hero-img') && heroItems.length > 0) {
            setHero(0);
        }
    } catch (e) { console.error("Slider load failed", e); }

    // 2. Fetch Side Cards
    try {
        const sideCardsData = await SupabaseClient.getSideCards();
        renderSideCards(sideCardsData);
    } catch (e) { console.error("Side cards load failed", e); }

    // 3. Fetch Items & Sections
    try {
        const sectionsData = await SupabaseClient.getSections();
        const itemsData = await SupabaseClient.getItems();
        renderInitialUI(sectionsData, itemsData, null);
    } catch (e) { console.error("Items/Sections load failed", e); }

    // 4. Fetch Minecraft Versions
    try {
        const versionsData = await SupabaseClient.getMinecraftVersions();
        if (document.getElementById('official-list')) {
            renderVersionsPage(versionsData);
        }
    } catch (e) { console.error("Versions load failed", e); }

    // Finalize UI
    try {
        initSearch();
        applyLanguage(currentLang);
        initFadeAnimations();
    } catch (e) { console.error("UI finalization failed", e); }
}

// Helper to render UI components
function renderInitialUI(sections, items, versions) {
    if (items && items.length > 0) {
        allItems = items.map(item => ({
            id: item.id,
            name: item.name_en,
            name_ar: item.name_ar,
            tag: item.tag,
            type: item.tag,
            img: item.img_url,
            file_size: item.file_size,
            version: item.version,
            download_url: item.download_url,
            description_en: item.description_en,
            description_ar: item.description_ar,
            gallery_images: item.gallery_images || [],
            gallery_videos: item.gallery_videos || []
        }));

        if (document.getElementById('skinGrid')) {
            renderSkinsPage(items.filter(i => i.tag === 'skins'));
        }

        if (document.querySelector('.worlds-grid') && !document.getElementById('all-mods-grid')) {
            renderWorldsPage(items.filter(i => i.tag === 'maps'));
        }

        if (document.getElementById('all-mods-grid')) {
            initAllModsGrid(items, sections);
        }
    }

    if (sections && sections.length > 0 && items && items.length > 0) {
        renderHomepageSections(sections, items);
    }

    if (versions && versions.length > 0 && document.getElementById('official-list')) {
        renderVersionsPage(versions);
    }
    
    // Ensure fade animations are applied to new elements
    initFadeAnimations();
}

function initFadeAnimations() {
    const observerOptions = {
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('show');
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    document.querySelectorAll('.fade').forEach(el => {
        observer.observe(el);
    });
}

function setHero(index) {
    if (heroItems.length === 0) return;
    currentHeroIndex = index;
    const item = heroItems[index];
    
    const img = document.getElementById('hero-img');
    const video = document.getElementById('hero-video');
    const youtube = document.getElementById('hero-youtube');
    const name = document.getElementById('hero-name');
    const metaInfo = document.getElementById('hero-meta-info');
    const tag = document.getElementById('hero-tag');
    const link = document.getElementById('hero-link');
    const heroInfo = document.getElementById('hero-info');

    if (!img) return;

    // Reset everything for the new slide
    clearTimeout(heroVideoTimeout);
    clearTimeout(heroAutoSlideTimeout);
    
    // IMMEDIATELY clear text to prevent overlap
    if (name) name.innerText = "";
    if (metaInfo) metaInfo.innerText = "";
    if (tag) tag.innerText = "";
    
    if (video) {
        video.pause();
        video.style.display = 'none';
        video.src = "";
    }
    if (youtube) {
        youtube.style.display = 'none';
        youtube.src = "";
    }
    
    // Show image and reset bar
    img.style.opacity = "1";
    if (heroInfo) {
        heroInfo.style.opacity = "1";
        heroInfo.style.display = "flex";
    }
    
    // Apply new data
    setTimeout(() => {
        img.src = item.img;
        
        const displayName = item.name[currentLang] || item.name.en;
        if (name) {
            name.innerText = displayName;
            name.setAttribute('data-original', displayName);
        }
        if (metaInfo) {
            const version = item.version || "1.21";
            const size = item.file_size || "0 MB";
            metaInfo.innerText = `${version} • ${size}`;
        }
        if (tag) {
            tag.innerText = translations[currentLang][item.tag] || item.tag.toUpperCase();
            tag.className = `tag tag-${item.tag}`;
        }
        if (link) {
            link.href = `item-details.html?id=${item.id}&name=${encodeURIComponent(item.name.en)}&img=${encodeURIComponent(item.img)}`;
        }
    }, 50); // Small delay to ensure text clear is visible

    // After 2 seconds, try to play video if it exists
    if (item.video) {
        heroVideoTimeout = setTimeout(() => {
            playHeroVideo(item.video);
        }, 2000);
    } else {
        heroAutoSlideTimeout = setTimeout(nextHero, 5000);
    }

    // Update dots
    const dotsContainer = document.getElementById('hero-dots');
    if (dotsContainer) {
        if (dotsContainer.children.length !== heroItems.length) {
            dotsContainer.innerHTML = heroItems.map((_, i) => `<div class="dot ${i === index ? 'active' : ''}" onclick="setHero(${i})"></div>`).join('');
        } else {
            const currentDots = dotsContainer.querySelectorAll('.dot');
            currentDots.forEach((dot, i) => dot.classList.toggle('active', i === index));
        }
    }
}

function playHeroVideo(videoSrc) {
    const video = document.getElementById('hero-video');
    const youtube = document.getElementById('hero-youtube');
    const img = document.getElementById('hero-img');
    
    // Clear any pending auto-slide when video starts
    clearTimeout(heroAutoSlideTimeout);

    if (videoSrc.includes('youtube.com') || videoSrc.includes('youtu.be')) {
        if (!youtube) return;
        let videoId = '';
        try {
            if (videoSrc.includes('watch?v=')) videoId = videoSrc.split('v=')[1].split('&')[0];
            else if (videoSrc.includes('youtu.be/')) videoId = videoSrc.split('/').pop().split('?')[0];
            else if (videoSrc.includes('youtube.com/embed/')) videoId = videoSrc.split('embed/')[1].split('?')[0];
            else if (videoSrc.includes('youtube.com/shorts/')) videoId = videoSrc.split('shorts/')[1].split('?')[0];
        } catch(e) {}
        
        if (videoId) {
            const origin = window.location.origin;
            youtube.src = `https://www.youtube.com/embed/${videoId}?autoplay=1&mute=1&controls=0&loop=1&playlist=${videoId}&rel=0&enablejsapi=1&origin=${encodeURIComponent(origin)}`;
            youtube.style.display = 'block';
            
            // Wait for YT player then hide image
            setTimeout(() => { 
                if (img) img.style.opacity = "0"; 
            }, 1000);

            // YouTube doesn't trigger "ended" easily, so slide after fixed duration
            heroAutoSlideTimeout = setTimeout(nextHero, 15000); 
        }
    } else {
        if (!video) return;
        video.src = videoSrc;
        video.style.display = 'block';
        video.play().then(() => {
            if (img) img.style.opacity = "0";
        }).catch(() => {
            video.style.display = 'none';
            heroAutoSlideTimeout = setTimeout(nextHero, 3000);
        });

        // For local video, wait until it finishes, then move to next slide
        video.onended = () => {
            video.style.display = 'none';
            if (img) img.style.opacity = "1";
            setTimeout(nextHero, 500);
        };
    }
}

function nextHero() {
    if (heroItems.length === 0) return;
    currentHeroIndex = (currentHeroIndex + 1) % heroItems.length;
    setHero(currentHeroIndex);
}

