let currentLang = localStorage.getItem('lang') || 'en';

function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    
    // Update HTML attributes
    document.documentElement.lang = lang;
    document.documentElement.dir = 'ltr';

    // Update elements with data-tr
    document.querySelectorAll('[data-tr]').forEach(el => {
        const key = el.getAttribute('data-tr');
        if (translations[lang][key]) {
            el.innerText = translations[lang][key];
        }
    });

    // Update titles with data-tr-title
    document.querySelectorAll('[data-tr-title]').forEach(el => {
        const key = el.getAttribute('data-tr-title');
        if (translations[lang][key]) {
            el.title = translations[lang][key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-tr-placeholder]').forEach(el => {
        const key = el.getAttribute('data-tr-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update tags automatically
    document.querySelectorAll('.tag').forEach(el => {
        let type = Array.from(el.classList).find(c => c.startsWith('tag-'))?.replace('tag-', '');
        if (type) {
            if (type === 'addon') type = 'addons';
            if (translations[lang][type]) {
                el.innerText = translations[lang][type];
            }
        }
    });

    // Automatically translate all card titles on the page
    document.querySelectorAll('.mod-card h3, .small-card h3, #hero-name').forEach(el => {
        let originalText = el.getAttribute('data-original');
        if (!originalText) {
            originalText = el.innerText.trim();
            el.setAttribute('data-original', originalText);
        }
        const trKey = originalText.toLowerCase().replace(/[^a-z0-9]+/g, '_').replace(/^_+|_+$/g, '');
        if (translations[lang][trKey]) {
            el.innerText = translations[lang][trKey];
        } else {
            el.innerText = originalText;
        }
    });

    // Refresh Hero Section if it exists
    if (document.getElementById('hero-img')) {
        setHero(currentHeroIndex);
    }
}

function toggleLanguage() {
    const newLang = currentLang === 'en' ? 'ar' : 'en';
    applyLanguage(newLang);
}

