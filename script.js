        // Filter items for this section
        const sectionItems = items.filter(i => i.section_id === section.id && !i.is_slider && !i.is_side_card).sort((a,b) => a.position - b.position);
        
        if (sectionItems.length === 0) return ''; // Hide empty sections
        
        const titleText = currentLang === 'ar' ? (section.title_ar || section.title_en) : section.title_en;
        const subtitleText = currentLang === 'ar' ? (section.subtitle_ar || section.subtitle_en) : section.subtitle_en;
