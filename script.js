/* SEARCH FUNCTIONALITY (Header Search) */

function initSearch() {
    const searchInput = document.querySelector('.nav-search-input');
    const searchGrid = document.getElementById('search-grid');
    const noResults = document.getElementById('no-results-placeholder');
    const searchClear = document.querySelector('.search-clear-icon');
    const searchIcon = document.querySelector('.search-icon-nav');

    if (!searchInput) return;

    const urlParams = new URLSearchParams(window.location.search);
    const query = urlParams.get('q');
    const isSearchPage = window.location.pathname.toLowerCase().includes('search.html');

    if (isSearchPage) {
        if (query) {
            searchInput.value = query;
            performSearch(query, searchGrid, noResults);
        } else {
            if (searchGrid) searchGrid.style.display = 'none';
            if (noResults) noResults.style.display = 'none';
        }
    }

    searchInput.addEventListener('keydown', (e) => {
        if (e.key === 'Enter') {
            triggerSearch();
        }
    });

    searchInput.addEventListener('input', () => {
        if (isSearchPage) {
            triggerSearch();
        }
    });

    if (searchIcon) {
        searchIcon.style.cursor = 'pointer';
        searchIcon.addEventListener('click', triggerSearch);
    }

    function triggerSearch() {
        const value = searchInput.value.toLowerCase().trim();
        const currentPath = window.location.pathname.toLowerCase();
        const onSearchPage = currentPath.includes('search.html');

        if (!onSearchPage) {
            if (value.length > 0) {
                window.location.href = `Search.html?q=${encodeURIComponent(value)}`;
            }
            return;
        }
        
        if (value === "") {
            if (searchGrid) searchGrid.style.display = 'none';
            if (noResults) noResults.style.display = 'none';
            return;
        }

        performSearch(value, searchGrid, noResults);
    }

    if (searchClear) {
        searchClear.addEventListener('click', () => {
            searchInput.value = "";
            if (isSearchPage) {
                if (searchGrid) searchGrid.style.display = 'none';
                if (noResults) noResults.style.display = 'none';
            }
            searchInput.focus();
        });
    }
}

function performSearch(value, searchGrid, noResults) {
    const query = value.toLowerCase().trim();
    const filtered = allItems.filter(item => 
        item.name.toLowerCase().includes(query) || 
        (item.name_ar && item.name_ar.toLowerCase().includes(query)) ||
        item.tag.toLowerCase().includes(query)
    );
    renderSearchResults(filtered, searchGrid, noResults);
}

function initSkinsSearch() {
    const skinSearch = document.getElementById('skinSearch');
    const skinGrid = document.getElementById('skinGrid');
    if (!skinSearch || !skinGrid) return;

    const skinCards = Array.from(skinGrid.querySelectorAll('.skin-card'));
    const skinsData = skinCards.map(card => {
        const idEl = card.querySelector('.skin-id-badge');
        return {
            html: card.outerHTML,
            name: card.querySelector('.skin-name').innerText.toLowerCase(),
            id: idEl ? idEl.innerText.toLowerCase() : ""
        };
    });

    skinSearch.addEventListener('input', (e) => {
        const value = e.target.value.toLowerCase().trim();
        
        if (value === "") {
            skinGrid.innerHTML = skinsData.map(s => s.html).join('');
            return;
        }

        const filtered = skinsData.filter(s => 
            s.name.includes(value) || 
            s.id.includes(value) || 
            s.id.replace('#', '').includes(value)
        );
        
        if (filtered.length > 0) {
            skinGrid.innerHTML = filtered.map(s => s.html).join('');
        } else {
            skinGrid.innerHTML = `<div style="grid-column: 1/-1; text-align: center; padding: 40px; color: #666;">${translations[currentLang]['no_matches'] || 'No skins found'}</div>`;
        }
    });
}

