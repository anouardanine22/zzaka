// Global Image Error Handler (for offline fallback to local images)
document.addEventListener('error', function(e) {
    if (e.target.tagName.toLowerCase() === 'img') {
        const localFallbacks = [
            'images/dark_fantasy.jpeg',
            'images/rlcraft_update.jpeg',
            'images/actions_stuff.jpeg',
            'images/essentials_card.png',
            'images/rlcraft_hero.png'
        ];
        const currentSrc = e.target.getAttribute('src');
        if (currentSrc && !localFallbacks.some(f => currentSrc.includes(f)) && !currentSrc.startsWith('data:')) {
            e.target.src = 'images/dark_fantasy.jpeg';
        }
    }
}, true);

